#include "../libft.h"

char	*ft_strdup(const char *s)
{
	int		i;
	int		len;
	char	*cpy;

	len = ft_strlen(s);
	cpy = ft_calloc(len + 1, sizeof(char));
	if (!cpy)
		return (NULL);
	i = 0;
	while (s[i])
	{
		cpy[i] = s[i];
		i++;
	}
	return (cpy);
}
